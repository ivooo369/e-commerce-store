export function getSpeedyOfficesByCity(cityName: string): SpeedyOffice[] {
  if (!cityName) return [];

  const normalizedSearch = cityName
    .toLowerCase()
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .trim();

  return speedyOffices.filter((office) => {
    const officeCity = office.city
      .toLowerCase()
      .normalize("NFD")
      .replace(/[\u0300-\u036f]/g, "");

    return officeCity.includes(normalizedSearch);
  });
}

export function getCitiesWithSpeedyOffices(): string[] {
  const cities = new Set<string>();
  speedyOffices.forEach((office) => {
    cities.add(office.city);
  });
  return Array.from(cities).sort();
}
